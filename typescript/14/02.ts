type Part = {
  material: string;
  quantity: number;
}

type Recipe = {
  outputQuantity: number;
  inputs: Part[];
}

const recipes: { [output: string]: Recipe } = {};

function getPart(input: string): Part {
  const quantityRegex = /[0-9]+/;
  const [quantityString] = input.match(quantityRegex);
  const quantity = Number(quantityString);

  const materialRegex = /[A-Z]+/;
  const [material] = input.match(materialRegex);

  return {
    quantity,
    material,
  };
}

function parseInput() {
  const partRegex = /(([0-9]+) ([A-Z]+))/g;

  for (const recipe of input.split('\n')) {
    const matches = recipe.match(partRegex);

    const output = matches[matches.length - 1];
    const inputs = [...matches];
    inputs.length = inputs.length - 1;

    const inputParts = inputs.map(input => getPart(input));
    const outputPart = getPart(output);

    recipes[outputPart.material] = {
      outputQuantity: outputPart.quantity,
      inputs: inputParts,
    };
  }
}

parseInput();

function getMaterialsNeededForMaterial(material: string, quantity: number) {
  const recipe = recipes[material];

  const materialsNeeded: { [key: string]: number } = recipe.inputs
    .reduce((acc, curr) => ({
      ...acc,
      [curr.material]: curr.quantity,
    }), {});

  const quantityMultiplier = Math.ceil(quantity / recipe.outputQuantity);

  for (const outputMaterial of Object.keys(materialsNeeded)) {
    materialsNeeded[outputMaterial] *= quantityMultiplier;
  }

  const excessiveQuantity = (recipe.outputQuantity * quantityMultiplier) - quantity;

  return { materialsNeeded, excessiveQuantity };
}

let excessiveMaterials: { [key: string]: number } = {};

function getOreNeededForMaterial(material: string) {
  const output = getMaterialsNeededForMaterial(material, 1);
  let materialsNeeded: { [key: string]: number } = output.materialsNeeded;

  while (true) {
    const nonOreMaterials = Object.keys(materialsNeeded).filter(m => m !== 'ORE');

    if (nonOreMaterials.length) {
      const newMaterialsNeeded: { [key: string]: number } = {
        ORE: materialsNeeded.ORE || 0,
      };
      for (const nonOreMaterial of nonOreMaterials) {
        while (excessiveMaterials[nonOreMaterial] > 0 && materialsNeeded[nonOreMaterial] > 0) {
          excessiveMaterials[nonOreMaterial] -= 1;
          materialsNeeded[nonOreMaterial] -= 1;
        }

        if (materialsNeeded[nonOreMaterial] === 0) {
          continue;
        }

        const ms = getMaterialsNeededForMaterial(nonOreMaterial, materialsNeeded[nonOreMaterial]);
        // console.log('ms eq', ms.excessiveQuantity);

        for (const m of Object.keys(ms.materialsNeeded)) {
          if (!newMaterialsNeeded[m]) {
            newMaterialsNeeded[m] = 0;
          }
          newMaterialsNeeded[m] += ms.materialsNeeded[m];
        }

        if (!excessiveMaterials[nonOreMaterial]) {
          excessiveMaterials[nonOreMaterial] = 0;
        }
        excessiveMaterials[nonOreMaterial] += ms.excessiveQuantity;
      }
      materialsNeeded = newMaterialsNeeded;
      // console.log('MATERIALS NEEDED', materialsNeeded);
      // console.log('EXCESSIVE MATERIALS', excessiveMaterials);
    } else {
      break;
    }
  }

  return materialsNeeded['ORE'];
}

